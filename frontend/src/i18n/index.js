const I18nContext = createContext({
  locale: "ru",
  setLocale: () => {},
  t: (key) => key,
});

const getBrowserLocale = () => {
  if (typeof window === "undefined") {
    return "ru";
  }

  const browserLanguage = (window.navigator.language || "ru").toLowerCase();
  return browserLanguage.startsWith("en") ? "en" : "ru";
};

const getInitialLocale = () => {
  if (typeof window === "undefined") {
    return "ru";
  }

  const storedLocale = window.localStorage.getItem(UI_LANGUAGE_STORAGE_KEY);
  if (storedLocale === "ru" || storedLocale === "en") {
    return storedLocale;
  }

  return getBrowserLocale();
};

const getMessageByPath = (locale, path) => {
  const dictionary = messages[locale] || messages.ru;
  const value = path.split(".").reduce((current, part) => (
    current && current[part] !== undefined ? current[part] : undefined
  ), dictionary);

  if (value !== undefined) {
    return value;
  }

  return path.split(".").reduce((current, part) => (
    current && current[part] !== undefined ? current[part] : undefined
  ), messages.ru);
};

const interpolate = (template, params = {}) => (
  String(template).replace(/\{\{(.*?)\}\}/g, (_, key) => {
    const paramKey = key.trim();
    return params[paramKey] ?? "";
  })
);

export function I18nProvider({ children }) {
  const [locale, setLocale] = useState(getInitialLocale);

  useEffect(() => {
    if (typeof window === "undefined") {
      return;
    }

    window.localStorage.setItem(UI_LANGUAGE_STORAGE_KEY, locale);
  }, [locale]);

  const value = useMemo(() => ({
    locale,
    setLocale,
    t: (path, params) => {
      const message = getMessageByPath(locale, path);
      if (typeof message === "string") {
        return interpolate(message, params);
      }
      return message;
    },
  }), [locale]);

  return (
    <I18nContext.Provider value={value}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}

export function getWordForms(locale) {
  const words = messages[locale]?.trainer?.words || messages.ru.trainer.words;
  return [words.one, words.few, words.many];
}
